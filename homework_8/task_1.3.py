#Task 3. Методы списка

fruits = ["apple", "banana"]

fruits.append("orange") #Добавьте "orange" в конец списка 
print(fruits)

fruits.insert(1,"grape") #Вставьте "grape" по индексу 1 
print(fruits)

fruits.remove("banana") #Удалите "banana" 
print(fruits)

fruits.sort() # Отсортируйте список
print(fruits)

fruits.reverse() # Переверните список 
print(fruits)